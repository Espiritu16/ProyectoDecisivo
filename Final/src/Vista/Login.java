    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // Obtener los valores de los campos de texto
    String correo = txtCorreo.getText();
    String contrasena = new String(txtContrasena.getPassword()); // Suponiendo que txtContrasena es un JPasswordField

    // Validación del correo electrónico usando una expresión regular
    if (!esCorreoValido(correo)) {
        JOptionPane.showMessageDialog(this, "Correo electrónico no válido. Por favor ingresa un correo válido.", "Error de Login", JOptionPane.ERROR_MESSAGE);
        return;  // Salir de la función si el correo no es válido
    }

    // Autenticar al usuario
    MantenerSession sessionManager = MantenerSession.getInstance();
    if (sessionManager.autenticarUsuario(correo, contrasena)) {
        // Si la autenticación es exitosa, mostrar el mensaje de bienvenida y abrir el JFrame correspondiente
        JOptionPane.showMessageDialog(this, "Bienvenido " + sessionManager.getNombre()+" Rol:"+sessionManager.getRol(), "Login Exitoso", JOptionPane.INFORMATION_MESSAGE);
        
        // Llamar al proxy para abrir el JFrame según el rol
        String rol = sessionManager.getRol(); // Obtener el rol del usuario (Usuario o Admin)
        UsuarioProxy usuarioProxy = new UsuarioProxy();
        usuarioProxy.abrirJFrameSegunRol(rol); // Abre el JFrame correspondiente según el rol

        // Cerrar la ventana de login (opcional)
        this.dispose();
    } else {
        // Si la autenticación falla, mostrar un mensaje de error
        JOptionPane.showMessageDialog(this, "Credenciales incorrectas", "Error de Login", JOptionPane.ERROR_MESSAGE);
    }
   
    }//GEN-LAST:event_btnIngresarActionPerformed

