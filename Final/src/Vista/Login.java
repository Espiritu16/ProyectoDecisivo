    // Actualizar el estado del formulario uso del patron Observer
    public void actualizarEstadoFormulario() {
        // Verifica que los campos de correo y contraseña estén llenos
        boolean formularioCompleto = !txtCorreo.getText().isEmpty() &&
                                      !new String(txtContrasena.getPassword()).isEmpty(); // Verifica que la contraseña no esté vacía

        // Habilita o deshabilita el botón "Ingresar" según el estado del formulario
        btnIngresar.setEnabled(formularioCompleto);
    }
    
    //validar que el correo sega @gmail.com.
    private boolean esCorreoValido(String correo) {
    // Expresión regular para validar el correo electrónico
    String regex = "^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*@(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,7}$";
    
    // Utiliza la expresión regular para validar el correo
    Pattern pattern = Pattern.compile(regex);
    Matcher matcher = pattern.matcher(correo);
    
    return matcher.matches();  // Retorna verdadero si el correo es válido
}
